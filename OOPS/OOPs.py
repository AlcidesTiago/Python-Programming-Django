'''
Features of Object Object Oriented Programming System (OOPS)
1. Classes and Objects
2. Encapsulation 
3. Abstraction
4. Inheritance
5. Polymorphism
'''
# This is a class
class Alc:
    # Attribute/Data Member/Variables
    a = 2
    b = 3
    # Actions/Method/Function/ Member Function
    def hi(self):
        print('hi')

    def hey(self):
        print('hey')

# Object/Instance of class
obj = Alc()
obj.hi()
obj.hey()